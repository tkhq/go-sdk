package generators

import (
	"fmt"
	"regexp"
	"strings"
)

var (
	activityVersionRE      = regexp.MustCompile(`_V[0-9]+$`)
	activityVersionGroupRE = regexp.MustCompile(`_V([0-9]+)$`)
)

func (g *generator) goName(raw string) string {
	if raw == "" {
		return ""
	}

	if name, ok := g.nameByRef[raw]; ok {
		return name
	}

	name := exportedName(stripLeadingVersion(normalizeProtoPackagePrefixes(raw)))
	if name == "" {
		name = "AutoGenerated"
	}

	if goKeywords[name] {
		name += "Value"
	}

	return name
}

func writeDoc(b *strings.Builder, text string, indent string) {
	text = sanitizeComment(text)
	if text == "" {
		return
	}

	for _, line := range strings.Split(text, "\n") {
		line = strings.TrimSpace(line)
		if line == "" {
			continue
		}

		fmt.Fprintf(b, "%s// %s\n", indent, line)
	}
}

func sanitizeComment(text string) string {
	text = strings.ReplaceAll(text, "\r\n", "\n")
	text = strings.ReplaceAll(text, "\r", "\n")

	return strings.TrimSpace(text)
}

func refName(ref string) string {
	return strings.TrimPrefix(ref, "#/definitions/")
}

func firstNonEmpty(values ...string) string {
	for _, value := range values {
		if strings.TrimSpace(value) != "" {
			return value
		}
	}

	return ""
}

func stripLeadingVersion(raw string) string {
	if len(raw) <= 1 || raw[0] != 'v' || raw[1] < '0' || raw[1] > '9' {
		return raw
	}

	i := 1
	for i < len(raw) && raw[i] >= '0' && raw[i] <= '9' {
		i++
	}

	return raw[i:]
}

func stripActivityVersion(activityType string) string {
	return activityVersionRE.ReplaceAllString(activityType, "")
}

// activityVersionSuffix returns the Go version suffix for an activity type:
// "ACTIVITY_TYPE_CREATE_USERS"    -> ""
// "ACTIVITY_TYPE_CREATE_USERS_V2" -> "V2"
func activityVersionSuffix(activityType string) string {
	match := activityVersionGroupRE.FindStringSubmatch(activityType)
	if match == nil {
		return ""
	}

	return "V" + match[1]
}

// resolveDefinitionName maps a Go-exported type name from activities.json back to its
// swagger definition key. Returns "" for empty or wildcard ("*") values.
// Names in activities.json use proto-style casing (e.g. CreateTvcAppIntent), so we
// normalize through exportedName to match the initialism-upper'd Go names that
// buildNameMap stores (e.g. CreateTVCAppIntent).
func (g *generator) resolveDefinitionName(goName string) string {
	if goName == "" || goName == "*" {
		return ""
	}

	normalized := exportedName(goName)
	if raw, ok := g.rawByGoName[normalized]; ok {
		return raw
	}

	return goName
}

func activityTypeFromOperation(operationName string) string {
	parts := splitIdentifier(operationName)
	if len(parts) == 0 {
		return ""
	}

	out := make([]string, 0, len(parts)+2)

	out = append(out, "ACTIVITY", "TYPE")
	for _, part := range parts {
		out = append(out, strings.ToUpper(part))
	}

	return strings.Join(out, "_")
}

func enumSuffix(typeName string, value string) string {
	value = strings.TrimSpace(value)
	prefix := upperSnake(typeName) + "_"
	value = strings.TrimPrefix(value, prefix)

	return exportedName(value)
}

func exportedName(raw string) string {
	return exportedNameFromParts(splitIdentifier(raw))
}

func normalizeProtoPackagePrefixes(raw string) string {
	replacer := strings.NewReplacer(
		"externalDataV1", "external_data_v1_",
		"externaldatav1", "external_data_v1_",
		"externalactivityv1", "external_activity_v1_",
		"commonV1", "common_v1_",
		"commonv1", "common_v1_",
		"dataV1", "data_v1_",
		"datav1", "data_v1_",
		"publicApiV1", "",
		"publicapiv1", "",
	)

	return replacer.Replace(raw)
}

func upperSnake(raw string) string {
	parts := splitIdentifier(raw)
	for i, part := range parts {
		parts[i] = strings.ToUpper(part)
	}

	return strings.Join(parts, "_")
}

func fieldName(raw string) string {
	name := exportedName(raw)
	if name == "" {
		return "Field"
	}

	if goKeywords[name] {
		return name + "Value"
	}

	return name
}

//nolint:gocyclo
func exportedNameFromParts(parts []string) string {
	var b strings.Builder

	prevUpper := ""

	for _, part := range parts {
		if part == "" {
			continue
		}

		upper := strings.ToUpper(part)

		// Skip consecutive duplicate segments (e.g. "api"+"Api" both become "API").
		if upper == prevUpper {
			continue
		}

		prevUpper = upper
		if strings.HasPrefix(upper, "OAUTH2") {
			b.WriteString("OAuth2")

			if len(part) > len("oauth2") {
				b.WriteString(exportedName(part[len("oauth2"):]))
			}

			continue
		}

		if upper == "OAUTH" {
			b.WriteString("OAuth")
			continue
		}

		if initialisms[upper] {
			b.WriteString(upper)
			continue
		}

		if strings.HasPrefix(upper, "V") && len(upper) > 1 {
			allDigits := true

			for _, r := range upper[1:] {
				if r < '0' || r > '9' {
					allDigits = false
					break
				}
			}

			if allDigits {
				b.WriteString(upper)
				continue
			}
		}

		b.WriteString(strings.ToUpper(part[:1]))

		if len(part) > 1 {
			b.WriteString(strings.ToLower(part[1:]))
		}
	}

	return b.String()
}

//nolint:gocyclo
func splitIdentifier(raw string) []string {
	raw = strings.TrimSpace(raw)
	if raw == "" {
		return nil
	}

	var (
		parts   []string
		current strings.Builder
	)

	runes := []rune(raw)
	flush := func() {
		if current.Len() == 0 {
			return
		}

		parts = append(parts, current.String())
		current.Reset()
	}

	for i, r := range runes {
		if !isIdentifierRune(r) {
			flush()
			continue
		}

		if current.Len() > 0 {
			prev := runes[i-1]
			nextLower := i+1 < len(runes) && runes[i+1] >= 'a' && runes[i+1] <= 'z'
			prevLowerOrDigit := (prev >= 'a' && prev <= 'z') || (prev >= '0' && prev <= '9')
			prevUpper := prev >= 'A' && prev <= 'Z'

			thisUpper := r >= 'A' && r <= 'Z'
			if thisUpper && (prevLowerOrDigit || (prevUpper && nextLower)) {
				flush()
			}
		}

		current.WriteRune(r)
	}

	flush()

	return parts
}

func isIdentifierRune(r rune) bool {
	return (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || (r >= '0' && r <= '9')
}

