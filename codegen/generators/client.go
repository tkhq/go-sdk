// Package generators contains code generation logic for the turnkey client.
package generators

import (
	"fmt"
	"strings"
)

func (g *generator) generateClient() string {
	var b strings.Builder
	b.WriteString(`// Code generated by go run ./codegen; DO NOT EDIT.

package turnkey

import "context"

`)

	for _, op := range g.operations {
		g.writeClientMethod(&b, op)
	}

	return b.String()
}

func (g *generator) writeClientMethod(b *strings.Builder, op operationInfo) {
	writeDoc(b, op.Description, "")

	if op.Deprecated {
		fmt.Fprint(b, "// Deprecated: this endpoint is marked deprecated in the Swagger spec.\n")
	}

	switch op.MethodType {
	case methodTypeCommand, methodTypeActivityDecision:
		inputType := op.MethodName + "Request"

		responseType := op.MethodName + "Response"
		if op.ResultDef != "" {
			resultType := g.goName(op.ResultDef)
			fmt.Fprintf(b, "func (c *Client) %s(ctx context.Context, input %s) (*%s, error) {\n", op.MethodName, inputType, responseType)
			fmt.Fprint(b, "\tbody, err := c.activityEnvelope(input)\n")
			fmt.Fprint(b, "\tif err != nil { return nil, err }\n")
			fmt.Fprint(b, "\tvar raw map[string]any\n")
			fmt.Fprintf(b, "\tif err := c.postJSON(ctx, c.config.urls.baseURL, %q, body, &raw, true, nil); err != nil { return nil, err }\n", op.Path)
			fmt.Fprintf(b, "\tout := new(ActivityResult[%s])\n", resultType)
			fmt.Fprintf(b, "\tif err := decodeActivityResponse(raw, %q, out); err != nil { return nil, err }\n", resultType)
			fmt.Fprintf(b, "\tresult, activity, err := activityAndWait(ctx, c, out, func(r Result) *%s { return r.%s })\n", resultType, resultType)
			fmt.Fprint(b, "\tif err != nil { return nil, err }\n")
			fmt.Fprintf(b, "\treturn &%s{Activity: *activity, %s: *result}, nil\n", responseType, resultType)
			fmt.Fprint(b, "}\n\n")
		} else {
			fmt.Fprintf(b, "func (c *Client) %s(ctx context.Context, input %s) (*%s, error) {\n", op.MethodName, inputType, responseType)
			fmt.Fprint(b, "\tbody, err := c.activityEnvelope(input)\n")
			fmt.Fprint(b, "\tif err != nil { return nil, err }\n")
			fmt.Fprint(b, "\tvar raw map[string]any\n")
			fmt.Fprintf(b, "\tif err := c.postJSON(ctx, c.config.urls.baseURL, %q, body, &raw, true, nil); err != nil { return nil, err }\n", op.Path)
			fmt.Fprint(b, "\tout := new(ActivityResult[map[string]any])\n")
			fmt.Fprintf(b, "\tif err := decodeActivityResponse(raw, %q, out); err != nil { return nil, err }\n", op.OperationName)
			fmt.Fprint(b, "\tactivity, err := activityAndWaitNoResult(ctx, c, out)\n")
			fmt.Fprint(b, "\tif err != nil { return nil, err }\n")
			fmt.Fprintf(b, "\treturn &%s{Activity: *activity}, nil\n", responseType)
			fmt.Fprint(b, "}\n\n")
		}

		fmt.Fprintf(b, "func (c *Client) Stamp%s(ctx context.Context, input %s) (*SignedRequest, error) {\n", op.MethodName, inputType)
		fmt.Fprint(b, "\tbody, err := c.activityEnvelope(input)\n")
		fmt.Fprint(b, "\tif err != nil { return nil, err }\n")
		fmt.Fprintf(b, "\treturn c.signedRequest(ctx, c.config.urls.baseURL, %q, body, true)\n", op.Path)
		fmt.Fprint(b, "}\n\n")
	case methodTypeProxy:
		inputType := g.goName(op.RequestDef)
		responseType := g.goName(op.ResponseDef)
		fmt.Fprintf(b, "func (c *Client) %s(ctx context.Context, input %s) (*%s, error) {\n", op.MethodName, inputType, responseType)
		fmt.Fprint(b, "\textraHeaders := map[string]string{}\n")
		fmt.Fprint(b, "\tif c.config.authProxyConfigID != \"\" { extraHeaders[\"X-Auth-Proxy-Config-ID\"] = c.config.authProxyConfigID }\n")
		fmt.Fprintf(b, "\tout := new(%s)\n", responseType)
		fmt.Fprintf(b, "\tif err := c.postJSON(ctx, c.config.urls.authProxyBaseURL, %q, input, out, false, extraHeaders); err != nil { return nil, err }\n", op.Path)
		fmt.Fprint(b, "\treturn out, nil\n")
		fmt.Fprint(b, "}\n\n")
	case methodTypeQuery:
		inputType := g.goName(op.RequestDef)
		responseType := g.goName(op.ResponseDef)

		if inputType == "" {
			inputType = goTypeMapStringAny
		}

		if responseType == "" {
			responseType = goTypeMapStringAny
		}

		fmt.Fprintf(b, "func (c *Client) %s(ctx context.Context, input %s) (*%s, error) {\n", op.MethodName, inputType, responseType)

		if op.RequestDef != "" && hasOrganizationID(g.definitions[op.RequestDef]) {
			fmt.Fprint(b, "\torganizationID, err := c.organizationID(input.OrganizationID)\n")
			fmt.Fprint(b, "\tif err != nil { return nil, err }\n")
			fmt.Fprint(b, "\tinput.OrganizationID = organizationID\n")
		}

		fmt.Fprintf(b, "\tout := new(%s)\n", responseType)
		fmt.Fprintf(b, "\tif err := c.postJSON(ctx, c.config.urls.baseURL, %q, input, out, true, nil); err != nil { return nil, err }\n", op.Path)
		fmt.Fprint(b, "\treturn out, nil\n")
		fmt.Fprint(b, "}\n\n")
	}
}

func hasOrganizationID(def *schema) bool {
	if def == nil {
		return false
	}

	_, ok := def.Properties["organizationId"]

	return ok
}
